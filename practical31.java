public class practical31 {
    public static void main(String[] args) {
        // Method 1: Math.abs()
        int absoluteValue = Math.abs(-10);
        System.out.println("Absolute value of -10: " + absoluteValue);

        // Method 2: Math.sqrt()
        double squareRoot = Math.sqrt(25);
        System.out.println("Square root of 25: " + squareRoot);

        // Method 3: Math.pow()
        double power = Math.pow(2, 3);
        System.out.println("2 raised to the power of 3: " + power);

        // Method 4: Math.max()
        int maximum = Math.max(10, 20);
        System.out.println("Maximum of 10 and 20: " + maximum);

        // Method 5: Math.min()
        int minimum = Math.min(30, 40);
        System.out.println("Minimum of 30 and 40: " + minimum);
    }
}